func (u *UdpPayloads) Len() int {
    if _, ok := u.payloads[0]; ok {
        return len(u.payloads) - 1
    }
    return len(u.payloads)
}



type Entry struct {
    Port    uint16
    Payload []byte
}



func (u *UdpPayloads) Entries() []Entry {
    entries := make([]Entry, 0, len(u.payloads))
    for port, payload := range u.payloads {
        if port == 0 {
            continue
        }
        entries = append(entries, Entry{Port: port, Payload: payload})
    }
    return entries
}



func (u *UdpPayloads) Get(port uint16) []byte {
    return u.payloads[port]
}